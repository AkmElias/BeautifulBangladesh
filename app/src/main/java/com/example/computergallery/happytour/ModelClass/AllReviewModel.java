package com.example.computergallery.happytour.ModelClass;



public class AllReviewModel {
    String reviewid;
    String comment;
    String placeId;
    String reviewuserEmail;
    String reviewuserName;
    String reviewuserImage;
    String reviewDate;
    String reviewRating;


    public String getId() {
        return reviewid;
    }

    public void setId(String reviewid) {
        this.reviewid = reviewid;
    }


    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getPlaceID() {
        return placeId;
    }

    public void setPlaceID(String placeId) {
        this.placeId = placeId;
    }

    public String getReviewUserEmail() {
        return reviewuserEmail;
    }

    public void setReviewUserEmail(String reviewuserEmail) {
        this.reviewuserEmail = reviewuserEmail;
    }

    public String getReviewUserName() {
        return reviewuserName;
    }

    public void setReviewUserName(String reviewuserName) {
        this.reviewuserName = reviewuserName;
    }

    public String getReviewUserImage() {
        return reviewuserImage;
    }

    public void setReviewUserImage(String reviewuserImage) {
        this.reviewuserImage = reviewuserImage;
    }

    public String getReviewRating() {
        return reviewRating;
    }

    public void setReviewRating(String reviewRating) {
        this.reviewRating = reviewRating;
    }

    public String getReviewDate() {
        return reviewDate;
    }
    public void setReviewDate(String reviewDate) {
        this.reviewDate = reviewDate;
    }



}
